#define _CRT_SECURE_NO_DEPRECATE
#include <stdio.h>
#include <locale.h>
//Условие:
//Роботы A и B должны начать работу одновременно, только если ровно один из номеров заданий является чётным.
//Формализация :
//Это значит, условие будет истинным, если :
//    (номер_A чётный И номер_B нечётный) ИЛИ
//    (номер_A нечётный И номер_B чётный)
int main() {

    setlocale(LC_ALL, "RUS");

    int taskA, taskB;
    int canStart;

    // Сообщаем пользователю, что делает программа
    printf("=== РАЗДЕЛЕНИЕ ОБЯЗАННОСТЕЙ ===\n");
    printf("Введите номера заданий для робота A и B: ");

    // Считываем номера заданий
    scanf("%d %d", &taskA, &taskB);

    // Вычисляем условие начала работы
    canStart = (taskA % 2 == 0 && taskB % 2 != 0) ||
        (taskA % 2 != 0 && taskB % 2 == 0);

    // Выводим результат
    printf("Роботы могут начать работу (1 - да, 0 - нет): %d\n", canStart);

    return 0;
}

